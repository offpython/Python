# 횟수에 의한 반복(for문)

# 대량 메일, 문자 발송
# 인사말 반복
# mp3 반복 재생

for i in range(5):
    print('Hello~')

for j in range(1, 10):
    result = 7 * j
    print('{} * {} = {}'.format(7, j, result))

for k in range(100):
    pass

for h in range(5):
    print('Hi', end = '')
    print(' loop statement')

# 실습
